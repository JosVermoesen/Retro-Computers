#include <ctypex.h>

/*--------------------------------- isalnum() -------------------------------*/

/*
 * isalnum tests for any charcter that is a decimal digit or an upper- or
 * lower-case alphabetic character.
 */

int (isalnum)(int c)
{
    return(isalnum(c));
}
