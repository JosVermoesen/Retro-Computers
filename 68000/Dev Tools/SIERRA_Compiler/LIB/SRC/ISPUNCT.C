#include <ctypex.h>

/*--------------------------------- ispunct() -------------------------------*/

/*
 * ispunct tests for any printing character that is neither space (' '), nor
 * a decimal digit, nor an upper- or lower-case alphabetic character.
 */

int (ispunct)(int c)
{
    return(ispunct(c));
}
