#include <ctypex.h>

/*-------------------------------- isxdigit() -------------------------------*/

/*
 * isxdigit tests for any hexadecimal digit (0 through 9, a through f, and
 * A through F)
 */

int (isxdigit)(int c)
{
    return(isxdigit(c));
}
