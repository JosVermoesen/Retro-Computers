#include <ctypex.h>

/*--------------------------------- _tolower() ------------------------------*/

/*
 * _tolower converts characters to lower-case, but if the argument to
 * _tolower() is not an upper-case character the result will be incorrect.
 */

int (_tolower)(int c)
{
    return(_tolower(c));
}
