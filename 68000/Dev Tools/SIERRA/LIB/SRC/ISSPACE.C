#include <ctypex.h>

/*--------------------------------- isspace() -------------------------------*/

/*
 * isspace tests for any character that is a standard white-space character.
 * The standard white-space characters are: space (' '), form feed ('\f'),
 * new-line ('\n'), carriage return ('\r'), horizontal tab ('\t') and
 * vertical tab ('\v').
 */

int (isspace)(int c)
{
    return(isspace(c));
}
