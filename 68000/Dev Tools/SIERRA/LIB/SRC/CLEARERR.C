#include <stdio.h>

/*------------------------------- clearerr() --------------------------------*/

/*
 * clearerr clears the end-of-file and error indicator for the stream pointed
 * to by fp
 */

void (clearerr)(register FILE *fp)
{
    clearerr(fp);
}
