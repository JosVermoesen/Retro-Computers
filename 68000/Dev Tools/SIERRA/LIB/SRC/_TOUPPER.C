#include <ctypex.h>

/*--------------------------------- _toupper() ------------------------------*/

/*
 * _toupper converts characters to upper-case, but if the argument to
 * _toupper() is not a lower-case character the result will be incorrect.
 */

int (_toupper)(int c)
{
    return(_toupper(c));
}
